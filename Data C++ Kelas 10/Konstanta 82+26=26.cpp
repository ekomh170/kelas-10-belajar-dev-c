#include<iostream.h>

using namespace std;

int main(){
     int m = 82,n = 26;
     cout<<m<<"+"<<n<<"="<<m+n<<endl;
     cout<<m<<"-"<<n<<"="<<m-n<<endl;
     cout<<m<<"*"<<n<<"="<<m*n<<endl;
     cout<<m<<"/"<<n<<"="<<m/n<<endl;
     cout<<m<<"%"<<n<<"="<<m%n<<endl;
     cout<<"-"<<m<<"="<<-m<<endl;
     
cin.get();
return 0;
}
